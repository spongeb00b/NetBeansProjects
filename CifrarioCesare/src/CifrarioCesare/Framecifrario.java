    private void antePrima(){
       if(primaRiga != null)
                testoAnteprima.setText(traduciRiga(primaRiga));
    }

    private String traduciRiga(String s){
        String output = "";
        int lettera ;
        for(int i = 0; i < s.length();i++ ){
            lettera = s.charAt(i);
            if(lettera >= vala && lettera <= valz)
                lettera = (lettera - vala + chiave + 26)%26+vala;
            if(lettera >= valA && lettera <= valZ)
                lettera = (lettera - valA + chiave + 26)%26+valA;
        output = output+(char)lettera;
       }
       return output;
   }
