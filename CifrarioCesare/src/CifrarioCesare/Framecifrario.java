    private void apriFilesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apriFilesActionPerformed
         if(selezionaFiles.showOpenDialog(this) == selezionaFiles.APPROVE_OPTION){
            input = selezionaFiles.getSelectedFile();
            BufferedReader lettoreBuffer = null;
            FileReader lettoreFile = null;
            try {
                lettoreFile = new FileReader(input);
                lettoreBuffer = new BufferedReader(lettoreFile);
                primaRiga = lettoreBuffer.readLine();
                if(primaRiga == null) throw new IOException("File Vuoto"); 
                    
            } 
            catch (FileNotFoundException ex) {etichettaStatus.setText("File Non Trovato!");
                                              input = null;
            }
            catch (IOException e) {etichettaStatus.setText("Errore"+e.getMessage());
                                    input = null;
            }
            finally{
                testoAnteprima.setText(primaRiga);
                if(lettoreBuffer != null)
                    try {
                    lettoreBuffer.close();
                } catch (IOException ex) {ex.printStackTrace();

                }
             if(lettoreFile != null)
                    try {
                    lettoreFile.close();
                } catch (IOException ex) {ex.printStackTrace();

                }
            }
         etichettaStatus.setText("File Aperto");
         chiave = 0;
         }


    }//GEN-LAST:event_apriFilesActionPerformed

    private void anteprimaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_anteprimaActionPerformed
        if(primaRiga != null)
            chiave++;
        antePrima();
    }//GEN-LAST:event_anteprimaActionPerformed

    private void decriptaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_decriptaActionPerformed
        if(input != null){
            BufferedReader lettoreBuffer = null;
            try {
                String temp;
                lettoreBuffer = new BufferedReader(new FileReader(input));
                testoDecript.setText("");
                try {
                    while ((temp = lettoreBuffer.readLine()) != null) {

                        testoDecript.append(traduciRiga(temp) + "\n");
                    }
                } catch (IOException ex) {
                    Logger.getLogger(Framecifrario.class.getName()).log(Level.SEVERE, null, ex);
                }


            }
            catch (FileNotFoundException ex) {etichettaStatus.setText("File Non Trovato!");
                                              input = null;}
        }
    }//GEN-LAST:event_decriptaActionPerformed

    private void salvaFilesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvaFilesActionPerformed
        if(!testoDecript.getText().equals(""))
            if(selezionaFiles.showSaveDialog(this) == selezionaFiles.APPROVE_OPTION){
                File output;
                output = selezionaFiles.getSelectedFile();
                PrintWriter printScrivi = null;
            try {
                printScrivi = new PrintWriter(new FileWriter(output), true);
                printScrivi.print(testoDecript.getText());
                etichettaStatus.setText("Salvataggio effettuato");
            } catch (IOException ex) { etichettaStatus.setText("");}
            finally{
                if(printScrivi != null)
                    printScrivi.close();

            }



            }






    }//GEN-LAST:event_salvaFilesActionPerformed

